    def get_data(self, city):
        city_t = self.city_trans.get(city)
        link = f"https://youla.ru/{city_t}/uslugi/uslugi"
        counter = 0
        r = requests.get(link)
        if r:
            cityId = self.get_city_id(r.text)
            serpId = self.get_serp_id(r.text)
            productName = self.get_product_name(r.text)
            if cityId and serpId and productName:
                page = 0
                while r.status_code != 404:
                    try:
                        page += 1
                        params = {
                            "city": cityId,
                            "page": f"{page}",
                            "q": productName,
                            "serpId": serpId
                        }
                        r = requests.get("https://youla.ru/web-api/products", params=params)
                        if r:
                            links = self.get_links(r.text)
                            counter += len(links)
                    except IndexError as e:
                        print(e)
                        break
                    except Exception as e:
                        if self.logs:
                            print(e)
                        continue
        return counter


