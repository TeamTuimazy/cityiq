    def get_city_id(self, data):
        i = 1
        while True:
            try:
                res = data.split('"cityId":"')[i].split('"')[0]
                if res:
                    return res
                i += 1
            except Exception as e:
                if self.logs:
                    print('get_city_id:', e)
                return True

    def get_serp_id(self, data):
        i = 1
        while True:
            try:
                res = data.split('"serpId":"')[i].split('"')[0]
                if res:
                    return res
                i += 1
            except Exception as e:
                if self.logs:
                    print('get_serp_id:', e)
                return True

    def get_product_name(self, data):
        i = 1
        while True:
            try:
                res = data.split('value="')[i].split('"')[0]
                if res:
                    return res
                i += 1
            except Exception as e:
                if self.logs:
                    print('get_product_name:', e)
                break

    def get_links(self, data):
        links = []
        i = 1
        while True:
            try:
                res = data.split('href=')[i].split(' ')[0].replace('\\', '') \
                        .replace('u0022', '')[0:-1]
                if ('https' not in res) and ('http' not in res):
                    links.append(res)
                i += 1
            except Exception as e:
                if self.logs:
                    print('get_links:', e)
                break
        return links

